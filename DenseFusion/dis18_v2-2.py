def extract_date_parts(date_str: str):
    """
    Extrahiert:
      - year
      - month
      - yyyymm

    aus ISO-Datum.
    """

    year, month, yyyymm = "", "", ""

    if date_str and len(date_str) >= 7:

        y = date_str[:4]
        m = date_str[5:7]

        if y.isdigit():

            year = y

            if m.isdigit() and 1 <= int(m) <= 12:

                month = m
                yyyymm = f"{y}{m}"

    return year, month, yyyymm


def iter_longeval_docs():
    """
    Kombiniert:
      - Snapshot 1
      - Snapshot 2
      - Snapshot 3

    zu einem vollständigen temporalen Korpus.

    Indexiert:
      - docno
      - text
      - year
      - month
      - yyyymm
    """

    datasets = [
        "longeval-sci-2026/snapshot-1",
        "longeval-sci-2026/snapshot-2",
        "longeval-sci-2026/snapshot-3"
    ]

    seen_docnos = set()

    for ds_name in datasets:

        print(f"\n[INDEX] Lade Dokumente aus: {ds_name}")

        ds = ir_datasets.load(ds_name)

        for i, d in enumerate(ds.docs_iter()):

            if i % 100000 == 0:
                print(f"processed: {i}")

            docno = str(d.doc_id)

            # Doppelte Dokumente verhindern
            if docno in seen_docnos:
                continue

            seen_docnos.add(docno)

            title = (d.title or "").strip()
            abstract = (d.abstract or "").strip()

            text = (
                title + "\n\n" + abstract
            ).strip()

            if not text:
                continue

            # Veröffentlichungsdatum
            published_date = getattr(
                d,
                "publishedDate",
                ""
            )

            year, month, yyyymm = (
                extract_date_parts(
                    published_date
                )
            )

            yield {
                "docno": docno,
                "text": text,
                "year": year,
                "month": month,
                "yyyymm": yyyymm
            }


