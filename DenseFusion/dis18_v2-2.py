# =========================================
DENSE_MODEL = None


def get_dense_model():

    global DENSE_MODEL

    if DENSE_MODEL is None:

        print(
            ">> Lade SentenceTransformer Modell..."
        )

        DENSE_MODEL = SentenceTransformer(
            "all-MiniLM-L6-v2"
        )

    return DENSE_MODEL


def add_dense_features_batched(
    cand,
    qdf,
    doc_texts,
    batch_size=64
):

    model = get_dense_model()

    unique_qids = (
        cand["qid"].unique()
    )

    qid_to_query = dict(
        zip(
            qdf["qid"],
            qdf["query"]
        )
    )

    q_texts = [
        qid_to_query[qid]
        for qid in unique_qids
    ]

    print(
        f">> Encodiere "
        f"{len(q_texts)} Queries..."
    )

    q_embs = model.encode(
        q_texts,
        batch_size=batch_size,
        convert_to_tensor=True,
        show_progress_bar=False
    )

    qid_to_emb = {
        qid: emb
        for qid, emb
        in zip(unique_qids, q_embs)
    }

    unique_docnos = (
        cand["docno"].unique()
    )

    d_texts = [
        doc_texts.get(d, "")[:1500]
        for d in unique_docnos
    ]

    print(
        f">> Encodiere "
        f"{len(d_texts)} Dokumente..."
    )

    d_embs = model.encode(
        d_texts,
        batch_size=batch_size,
        convert_to_tensor=True,
        show_progress_bar=True
    )

    docno_to_emb = {
        docno: emb
        for docno, emb
        in zip(unique_docnos, d_embs)
    }

    print(">> Berechne Dense Scores...")

    dense_scores = []

    for _, row in cand.iterrows():

        q_e = qid_to_emb[row["qid"]]
        d_e = docno_to_emb[row["docno"]]

        score = util.cos_sim(
            q_e,
            d_e
        ).item()

        dense_scores.append(score)

    cand["f_dense"] = dense_scores

    return cand


# =========================================
# MAIN
# =========================================
def main():

    ensure_java()

    print("Loading datasets...")

    # =====================================
    # TRAINING
    # =====================================

    ds_train = ir_datasets.load(
        "longeval-sci-2026/snapshot-1/train/dctr"
    )

    # =====================================
    # DOC CORPORA
    # =====================================

    ds_docs_s1 = ir_datasets.load(
        "longeval-sci-2026/snapshot-1"
    )

    ds_docs_s2 = ir_datasets.load(
        "longeval-sci-2026/snapshot-2"
    )

    ds_docs_s3 = ir_datasets.load(
        "longeval-sci-2026/snapshot-3"
    )

    # =====================================
    # SNAPSHOT 3 QUERIES
    # =====================================

    ds_test = ir_datasets.load(
        "longeval-sci-2026/snapshot-3"
    )

    # =====================================
    # QUERIES + QRELS
    # =====================================

    q_train = pd.DataFrame([
        {
            "qid": q.query_id,
            "query": clean_query(q.text)
        }
        for q in ds_train.queries_iter()
    ])

    qrels = pd.DataFrame([
        {
            "qid": q.query_id,
            "docno": q.doc_id,
            "label": int(q.relevance)
        }
        for q in ds_train.qrels_iter()
    ])

    q_test = pd.DataFrame([
        {
            "qid": q.query_id,
            "query": clean_query(q.text)
        }
        for q in ds_test.queries_iter()
    ])

    print(
        f"Train Queries: {len(q_train)} | "
        f"Snapshot3 Queries: {len(q_test)}"
    )

    # =====================================
    # INDEX
    # =====================================

    if not INDEX_DIR.exists():

        raise FileNotFoundError(
            "Index fehlt!"
        )

    index_path = str(
        INDEX_DIR.resolve()
    )

    print(
        "USING INDEX:",
        index_path
    )

    index = pt.IndexFactory.of(
        index_path
    )

    bm25 = pt.terrier.Retriever(
        index,
        wmodel="BM25",
        num_results=CAND_TOPK
    )

    # =====================================
    # RETRIEVAL
    # =====================================

    print("\n--- RETRIEVAL ---")

    cand_train = bm25.transform(
        q_train
    )

    cand_test = bm25.transform(
        q_test
    )

    # =====================================
    # FEATURE EXTRACTION
    # =====================================

    print(
        "\n--- FEATURE EXTRACTION ---"
    )

    doc_lens, doc_texts, doc_titles = (
        build_doc_caches([
            ds_docs_s1,
            ds_docs_s2,
            ds_docs_s3
        ])
    )

    get_yyyymm = build_meta_lookup(
        INDEX_DIR
    )

    (
        log_citation_map,
    ) = load_citation_features()

    cand_train = add_features(
        cand_train,
        q_train,
        doc_lens,
        doc_texts,
        doc_titles,
        get_yyyymm,
        log_citation_map
    )

    cand_test = add_features(
        cand_test,
        q_test,
        doc_lens,
        doc_texts,
        doc_titles,
        get_yyyymm,
        log_citation_map
    )

    # Dense Features
    cand_train = add_dense_features_batched(
        cand_train,
        q_train,
        doc_texts
    )

    cand_test = add_dense_features_batched(
        cand_test,
        q_test,
        doc_texts
    )

    # =====================================
    # TRAIN LTR
    # =====================================

    print("\n--- TRAIN LTR ---")

    train_df = cand_train.merge(
        qrels,
        on=["qid", "docno"],
        how="left"
    )

    train_df["label"] = (
        train_df["label"]
        .fillna(0)
    )

    train_df = (
        train_df
        .sort_values(
            ["qid", "score"],
            ascending=[True, False]
        )
        .groupby("qid")
        .head(TRAIN_TOPK)
    )

    feature_cols = [
        "f_bm25",
        "f_qlen",
        "f_doclen",
        "f_recency",
        "f_overlap",
        "f_title_match",
        "f_dense",
        "f_log_citation_count",
    ]

    X = train_df[feature_cols]
    y = train_df["label"]

    group_sizes = (
        train_df
        .groupby("qid")
        .size()
        .tolist()
    )

    import lightgbm as lgb

    lgb_train = lgb.Dataset(
        X,
        label=y,
        group=group_sizes,
        free_raw_data=False
    )

    params = {
        "objective": "lambdarank",
        "metric": "ndcg",
        "ndcg_eval_at": [10],
        "learning_rate": MODEL_LEARNING_RATE,
        "num_leaves": MODEL_NUM_LEAVES,
        "verbosity": -1,
    }

    model = lgb.train(
        params,
        lgb_train,
        num_boost_round=MODEL_NUM_TREES
    )

    # =====================================
    # SNAPSHOT 3 INFERENCE
    # =====================================

    print(
        "\n--- SNAPSHOT 3 INFERENCE ---"
    )

    # BM25
    bm25_test = cand_test.sort_values(
        ["qid", "score"],
        ascending=[True, False]
    )

    bm25_test["new_rank"] = (
        bm25_test
        .groupby("qid")
        .cumcount()
    )

    write_run_jsonl(
        bm25_test,
        RUN_BM25,
        "score"
    )

    # LTR
    ltr_test = cand_test.copy()

    ltr_test["ltr_score"] = (
        model.predict(
            ltr_test[feature_cols]
        )
    )

    ltr_test["bm25_norm"] = (
        ltr_test
        .groupby("qid")["score"]
        .transform(
            lambda x:
            (x - x.min())
            / (x.max() - x.min())
            if x.max() > x.min()
            else 0.0
        )
    )

    ltr_test["ltr_norm"] = (
        ltr_test
        .groupby("qid")["ltr_score"]
        .transform(
            lambda x:
            (x - x.min())
            / (x.max() - x.min())
            if x.max() > x.min()
            else 0.0
        )
    )

    # Hybrid
    ltr_test["hybrid_score"] = (
        (1 - BEST_ALPHA)
        * ltr_test["bm25_norm"]
        + BEST_ALPHA
        * ltr_test["ltr_norm"]
    )

    ltr_test = ltr_test.sort_values(
        ["qid", "hybrid_score"],
        ascending=[True, False]
    )

    ltr_test["new_rank"] = (
        ltr_test
        .groupby("qid")
        .cumcount()
    )

    write_run_jsonl(
        ltr_test,
        RUN_HYBRID,
        "hybrid_score"
    )

    print("\nFertig!")

    print(
        f"BM25 Run: {RUN_BM25}"
    )

    print(
        f"Hybrid Run: {RUN_HYBRID}"
    )

    print("\nHinweis:")

    print(
        "Snapshot 3 besitzt "
        "keine öffentlichen Qrels."
    )

    print(
        "Daher wird keine "
        "nDCG@10 Evaluation berechnet."
    )


if __name__ == "__main__":
    main()