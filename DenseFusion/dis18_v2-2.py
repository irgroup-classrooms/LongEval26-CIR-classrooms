CITATION_FEATURES = None


def load_citation_features(
    csv_path=CITATION_CSV
):

    global CITATION_FEATURES

    if CITATION_FEATURES is not None:
        return CITATION_FEATURES

    print(">> Lade Citation Features...")

    df = pd.read_csv(csv_path)

    df["doc_id"] = (
        df["doc_id"].astype(str)
    )

    df["f_log_citation_count"] = (
        np.log1p(df["citation_count"])
    )

    log_citation_map = dict(
        zip(
            df["doc_id"],
            df["f_log_citation_count"]
        )
    )

    CITATION_FEATURES = (
        log_citation_map,
    )

    return CITATION_FEATURES


# =========================================
# FEATURE EXTRACTION
# =========================================
def add_features(
    cand,
    qdf,
    doc_lens,
    doc_texts,
    doc_titles,
    get_yyyymm,
    log_citation_map
):

    qlen_map = dict(
        zip(
            qdf["qid"],
            qdf["query"].map(qlen)
        )
    )

    cand["f_qlen"] = (
        cand["qid"]
        .map(qlen_map)
        .fillna(0)
        .astype(int)
    )

    cand["f_bm25"] = (
        cand["score"].astype(float)
    )

    cand["f_doclen"] = (
        cand["docno"]
        .map(doc_lens)
        .fillna(0)
    )

    cand["f_log_citation_count"] = (
        cand["docno"]
        .astype(str)
        .map(log_citation_map)
        .fillna(0)
    )

    cand["yyyymm"] = (
        cand["docid"]
        .map(get_yyyymm)
    )

    def calc_age_in_months(yymm):

        if yymm == 0:
            return 999

        y = yymm // 100
        m = yymm % 100

        return (
            (2026 - y) * 12
            + (4 - m)
        )

    cand["f_recency"] = (
        cand["yyyymm"]
        .map(calc_age_in_months)
    )

    def calc_overlap(row, text_dict):

        q_terms = set(
            str(row["query"]).split()
        )

        doc_text = text_dict.get(
            row["docno"],
            ""
        )

        if not doc_text:
            return 0.0

        doc_terms = set(
            doc_text.split()
        )

        overlap = len(
            q_terms.intersection(doc_terms)
        )

        return overlap / max(
            1,
            len(q_terms)
        )

    if "query" not in cand.columns:

        cand = cand.merge(
            qdf[["qid", "query"]],
            on="qid",
            how="left"
        )

    cand["f_overlap"] = cand.apply(
        lambda r: calc_overlap(
            r,
            doc_texts
        ),
        axis=1
    )

    cand["f_title_match"] = cand.apply(
        lambda r: calc_overlap(
            r,
            doc_titles
        ),
        axis=1
    )

    cand.drop(
        columns=["yyyymm"],
        inplace=True
    )

    return cand


