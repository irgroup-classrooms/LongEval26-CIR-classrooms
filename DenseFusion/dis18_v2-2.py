            abstract = (d.abstract or "").strip()
            text = (title + "\n\n" + abstract).strip()

            if not text:
                continue

            # Sicherstellen, dass das Attribut existiert
            published_date = getattr(d, 'publishedDate', '')
            year, month, yyyymm = extract_date_parts(published_date)

            yield {
                "docno": docno,
                "text": text,
                "year": year,
                "month": month,
                "yyyymm": yyyymm
            }


